function getArabicName(englishName, type = 'district') {
  if (!englishName) return '';
  
  const map = type === 'district' ? IRAQ_TRANSLATIONS.districts : IRAQ_TRANSLATIONS.provinces;
  return map[englishName] || englishName;
}